public class GameEntity {
    private String name ;
    private int healhs;
    private int damag ;
    public GameEntity(){

    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getHealhs() {
        return healhs;
    }

    public void setHealhs(int healhs) {
        this.healhs = healhs;
    }

    public int getDamag() {
        return damag;
    }

    public void setDamag(int damag) {
        this.damag = damag;
    }
}
